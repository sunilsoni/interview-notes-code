package com.interview.notes.code.year.y2024.march24.test7;

public class OperatorsDemo {
    public static void main(String[] args) {
        boolean b = false;
        int n = 5;
        System.out.println(b || n == 5);
        System.out.println(b = true && n == 5);
        System.out.println(b);
    }
}
