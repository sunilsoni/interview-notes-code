package com.interview.notes.code.year.y2024.aug24.test32;

class Car {
    private final String name;
    private final int year;
    private final int miles;

    // Constructor
    public Car(String name, int year, int miles) {
        this.name = name;
        this.year = year;
        this.miles = miles;
    }

    // Getters
    public String getName() {
        return name;
    }

    public int getYear() {
        return year;
    }

    public int getMiles() {
        return miles;
    }
}
