package com.interview.notes.code.months.aug24.test32;

import java.util.regex.Pattern;

public class IPv4Validator1 {
    final static String regularExpression = "^(([0-9]|[1-9][0-9]|1[0-9]{2}|2[0-4][0-9]|25[0-5])|0{1,3}[0-9]|0{1,2}[1-9][0-9]|0[1-9][0-9]{2})\\.((([0-9]|[1-9][0-9]|1[0-9]{2}|2[0-4][0-9]|25[0-5])|0{1,3}[0-9]|0{1,2}[1-9][0-9]|0[1-9][0-9]{2})\\.){2}([0-9]|[1-9][0-9]|1[0-9]{2}|2[0-4][0-9]|25[0-5]|0{1,3}[0-9]|0{1,2}[1-9][0-9]|0[1-9][0-9]{2})$";

    public static void main(String[] args) {
        String[] testCases = {
                "000.12.12.034",
                "121.234.12.12",
                "23.45.12.56",
                "00.12.123.123123.123",
                "122.23",
                "Hello.IP",
                "222.030.151.166",
                "186.116.66.090",
                "56.224.195.010",
                "59.153.14.022",
                "65.217.244.236",
                "124.255.114.193",
                "253.110.43.190",
                "768540180.9506559.974145554.987289718",
                "499235237.88055889.436631985.957967894.957967894",
                "475974065.826049509.610050395.822874346.822874346",
                "74748765.592816709.897246293.334470435",
                "561983629.88164781.712443660.543233267",
                "82659376.679462273.361182727.522428069",
                "562551020.186224077.361204300.858079639",
                "199394305.598510508.83518696.863140814.863140814",
                ".1.1.",
                ".1.1.1",
                "1.1..1",
                "1...",
                "999.999.99.9",
                "111.111.1.1.11",
                "001a100b11.123",
                "100x234.255.0",
                "0000.00.00.00",
                "0034.0.0.0"
        };

        for (String testCase : testCases) {
            System.out.println(testCase + ": " + (Pattern.matches(regularExpression, testCase) ? "Valid" : "Invalid"));
        }
    }
}
